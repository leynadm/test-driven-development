const calculator = {

    sum(a,b){
        let result = a + b
        return result
    },
 
    subtract(a,b){
        let result = a - b
        return result
    },

    divide(a,b){
        let result = a / b
        return result
    },

    multiply(a,b){
        let result = a * b
        return result
    },

}


module.exports = calculator